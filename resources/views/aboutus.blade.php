                <h3>The “Neighbours: St. John’s” Phone App</h3>
                <p>
                    We collaborated with award-winning radio documentarian Chris Brookes
                    <a href="http://batteryradio.weebly.com"> (Battery Radio) </a> to develop a free
                    phone app that makes available select stories from the St. John’s Many Voices, as well as
                    additional interviews conducted in 2016 by Chris Brookes, Beverley Diamond, and Meghan
                    Forsyth. Stories in the archive focus on immigration, belonging, and cultural connections. The
                    primary content of the app is 27 audio narratives, which are accompanied by new and archival
                    photographs, storyteller bios, community portraits, and links to related websites and online
                    videos.
                </p>
                <p>
                    The stories cover an extremely wide range of topics. You will hear stories about capelin,
                    Aboriginal culture, fashion dilemmas, raising children, and Minute Rice. You will hear from
                    Tony Silver, whose father was a stowaway on a Portuguese fishing ship and ended up in St.
                    John’s, where he founded a tailor shop on Freshwater Road. Mi’kmaq artist Jerry Evans talks
                    about the Shanawdithit monument in Bannerman Park and his Beothuk and Mi’kmaq heritage.
                    He relates his frustration with how Newfoundland and Labrador culture is often depicted as “all
                    things Irish.” You will also hear about Cubans defecting at Gander and a young woman’s
                    experience of wearing the hijab. These are just a few of many fascinating accounts featured on
                    the app.
                </p>
                <p>
                    You can access the app either as you walk downtown St. John’s (and some outlying
                    areas, such as Middle Cove Beach), or remotely, from the comfort of home. If the app is turned
                    on, your phone’s GPS will automatically alert you to a story that is connected to your location.
                    Using GPS, the app will not draw on your phone’s data plan.
                </p>
