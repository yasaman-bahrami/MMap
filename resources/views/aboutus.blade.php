                <h2>
                    Neighbours: St. John’s
                </h2>
                <p>
                    Neighbours: St. John’s is an interactive archive of stories from cultural communities in and
                    around St. John’s (Canada). It is part of a larger project about cultural diversity in our province
                    that has also resulted in a smartphone app. The stories included on this website range from
                    comical reflections on local traditions and experiences to tales that address the challenges faced
                    by newcomers to the province. Some of the stories are celebratory in tone, while others are more
                    challenging. We hope that they will inspire users of this website to listen more deeply to their
                    neighbours.
                </p>
                <h3>
                    Background
                </h3>
                <p>
                    Between 2009 and 2011, the Research Centre for the Study of Music, Media, and Place
                    (MMaP) at Memorial University of Newfoundland conducted a research project entitled St.
                    John’s Many Voices. Led by MMaP’s founding director, Dr. Beverley Diamond, a team of
                    researchers conducted over ninety interviews with individuals from diverse ethnic and religious
                    communities.
                </p>
                <p>
                    The resulting oral history archive contains interviews with individuals from diverse
                    ethnic and religious communities, as well as manuscripts, photographs, archival audiovisual
                    material, and new performance materials.
                </p>
                <p>
                    In 2014, MMaP hosted a day-long meeting with community stakeholders about the
                    creation of one or more exhibitions on cultural diversity, which would draw from the earlier
                    project. In attendance were representatives from a wide range of groups, including immigrant
                    organizations, museums and archives, city and provincial government departments, exhibit
                    designers, academic colleagues from across Memorial University of Newfoundland, the Harris
                    Centre for Regional Policy and Development, and the Office of Public Engagement at Memorial.
                    The meeting focused primarily on the question of how to extend the visibility of diversity in our
                    city. The idea of “neighbours” was adopted as an unifying theme. The participants felt that
                    projects emerging under the umbrella of the Neighbours Project should address questions of
                    belonging, different kinds of cultural alliances and connections (including intergenerational and
                    cross cultural ones), and ethnic diversity. The meeting participants emphasized that the project
                    should produce exhibits that are public, mobile, and interactive, and should speak to a wide range
                    of audiences.
                </p>
